import re

# 元字符--正则表达式中具有特殊含义的字符

d = "139294119221375846685513654888655"

pat1 = "...."                    # . 匹配任意字符  \n除外

pat2 = r"^139\d\d\d\d\d\d\d\d"  # ^ 匹配字符串的开始位置 ^139

pat3 = "8655$"                  # $ 匹配字符串的结束位置

pat4 = "8?"                     # ? 重复0次或一次前面的原子

pat5 = "8*"                     # * 重复0次或一次或多次前面的原子

pat6 = "58+"                     # + 重复一次或多次前面的原子

print(re.search(pat3, d))
